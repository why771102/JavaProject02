package com.Bean;

public class VenueBean {

	private int VendorID;
	private int ProductID;
	private String VenueAddress;
	private String Hall;
	private int Tables;
	private int InOutdoor;
	private int LunchPrice;
	private int DinnerPrice;
	public int getVendorID() {
		return VendorID;
	}
	public void setVendorID(int vendorID) {
		VendorID = vendorID;
	}
	public int getProductID() {
		return ProductID;
	}
	public void setProductID(int productID) {
		ProductID = productID;
	}
	public String getVenueAddress() {
		return VenueAddress;
	}
	public void setVenueAddress(String venueAddress) {
		VenueAddress = venueAddress;
	}
	public String getHall() {
		return Hall;
	}
	public void setHall(String hall) {
		Hall = hall;
	}
	public int getTables() {
		return Tables;
	}
	public void setTables(int tables) {
		Tables = tables;
	}
	public int getInOutdoor() {
		return InOutdoor;
	}
	public void setInOutdoor(int inOutdoor) {
		InOutdoor = inOutdoor;
	}
	public int getLunchPrice() {
		return LunchPrice;
	}
	public void setLunchPrice(int lunchPrice) {
		LunchPrice = lunchPrice;
	}
	public int getDinnerPrice() {
		return DinnerPrice;
	}
	public void setDinnerPrice(int dinnerPrice) {
		DinnerPrice = dinnerPrice;
	}
	
	
}
